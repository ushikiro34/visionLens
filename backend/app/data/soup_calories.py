# 국물 음식 키워드 판별 (음식명에 포함되면 국물 음식으로 처리)
SOUP_KEYWORDS = [
    "탕", "국", "찌개", "전골", "라면", "우동", "쌀국수", "냉면",
    "국밥", "순댓국", "돼지국밥", "갈비탕", "삼계탕", "해장국",
]


def get_soup_calories(food_name: str) -> tuple[int, int, str] | None:
    """
    음식명으로 고정 칼로리 조회.
    정확한 매칭 우선, 없으면 키워드 부분 매칭.
    반환: (calories_kcal, mass_g, description) 또는 None
    """
    # 정확한 매칭
    if food_name in SOUP_CALORIES:
        return SOUP_CALORIES[food_name]

    # 부분 매칭 (가장 긴 키 우선)
    matches = [(k, v) for k, v in SOUP_CALORIES.items() if k in food_name]
    if matches:
        best = max(matches, key=lambda x: len(x[0]))
        return best[1]

    return None


def is_soup_food(food_name: str) -> bool:
    """국물 음식 여부 판별"""
    if get_soup_calories(food_name):
        return True
    return any(kw in food_name for kw in SOUP_KEYWORDS)
